package com.goodhuddle.huddle.domain;

public class ThemeSettingsField {

    private String code;
    private String name;
    private ThemeSettingType type;

    public ThemeSettingsField(String code, String name, ThemeSettingType type) {
        this.code = code;
        this.name = name;
        this.type = type;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public ThemeSettingType getType() {
        return type;
    }

    public void setType(ThemeSettingType type) {
        this.type = type;
    }
}
