package com.goodhuddle.huddle.web.dto.theme;

public class ThemeDetail {

    private long id;
    private String name;
    private String description;
    private boolean active;

    public ThemeDetail(long id, String name, String description, boolean active) {
        this.id = id;
        this.name = name;
        this.description = description;
        this.active = active;
    }

    public long getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getDescription() {
        return description;
    }

    public boolean isActive() {
        return active;
    }
}
