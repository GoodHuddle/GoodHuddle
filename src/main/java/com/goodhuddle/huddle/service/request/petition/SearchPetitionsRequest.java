package com.goodhuddle.huddle.service.request.petition;

public class SearchPetitionsRequest {

    private int page;
    private int size;

    public SearchPetitionsRequest() {
        page = 0;
        size = 20;
    }

    public SearchPetitionsRequest(int page, int size) {
        this.page = page;
        this.size = size;
    }

    public int getPage() {
        return page;
    }

    public void setPage(int page) {
        this.page = page;
    }

    public int getSize() {
        return size;
    }

    public void setSize(int size) {
        this.size = size;
    }

    @Override
    public String toString() {
        return "SearchPetitionsRequest[page=" + page + ",size=" + size + "]";
    }
}
