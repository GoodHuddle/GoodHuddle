package com.goodhuddle.huddle.service.request.menu;

public class UpdateMenuItemDetailsRequest {

    private long id;
    private String label;
    private String url;

    public UpdateMenuItemDetailsRequest() {
    }

    public UpdateMenuItemDetailsRequest(long id, String label, String url) {
        this.id = id;
        this.label = label;
        this.url = url;
    }

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public String getLabel() {
        return label;
    }

    public void setLabel(String label) {
        this.label = label;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }
}
